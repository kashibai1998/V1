
public class Organization {
	
	//Instance variables
	private String name;
	
	//Default COnstructor
	public Organization() {
		
	}

	//Parameterised constructor
	public Organization(String name) {
		super();
		this.name = name;
	}

	//getter and setter methods
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	
	
}
